#define BH1570_ADDR     0x23
#define BH1570_POWERON  0x01
#define BH1570_RESET    0x07
#define BH1570_MODE     0x10
#define BH1570_READ     (BH1570_ADDR<<1 | 0x01)
#define BH1570_WRITE    (BH1570_ADDR<<1)
#define I2C_HANDLE      &hi2c1
#define TIME_OUT        1000
#define ERROR()     do {while(1){};} while(0);
static  uint16_t Lux_Data = 0;
static void Bh1570_On (void)
{
  send_cmd(BH1570_POWERON);
  send_cmd(BH1570_RESET);
  send_cmd(BH1570_MODE);
  HAL_Delay(100);
}
 void send_cmd (uint8_t cmd)
{

  if (HAL_I2C_Master_Transmit(I2C_HANDLE,BH1570_WRITE,cmd,1,TIME_OUT) != HAL_OK)
  {
    ERROR();
  }
}
static void read_const_measurement(uint8_t* Dta)
{
  if(HAL_I2C_Master_Receive(I2C_HANDLE,BH1570_READ,Dta,2,TIME_OUT) != HAL_OK)
  {
    ERROR();
  }
}
uint16_t Read_Data(void)
{
  uint8_t raw_data[2] = {0};
  read_const_measurement(&raw_data[0]);
  return (raw_data[0]<<8 | raw_data[1]);
}
void unit_test(void)
{
  Bh1570_On();
  while(1)
  {
    Lux_Data = Read_Data();
    HAL_Delay(50);
  }
}

